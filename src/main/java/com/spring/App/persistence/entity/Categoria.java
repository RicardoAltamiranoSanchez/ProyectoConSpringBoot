package com.spring.App.persistence.entity;


import javax.persistence.*;
import java.util.List;


@Entity
@Table(name = "categorias")
public class Categoria {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "id_categoria")
    private Integer idCategoria;

    private String descripcion;
    private Boolean estado;
// ponemos el nombre de nuestra variable donde tenemos la relacion y hicimos el Many
    @OneToMany(mappedBy = "categoriaRelacion") //indicamos que es de uno a muchos para la relacion de nuestra entitys
    private List<Producto> productos;//inicamos que es una lista de productos




    public Integer getIdCategoria() {
        return idCategoria;
    }
    public void setIdCategoria(Integer idCategoria) {
        this.idCategoria = idCategoria;
    }
    public String getDescripcion() {
        return descripcion;
    }
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
    public Boolean getEstado() {
        return estado;
    }
    public void setEstado(Boolean estado) {
        this.estado = estado;
    }
    public List<Producto> getProductos() {
        return productos;
    }

    public void setProductos(List<Producto> productos) {
        this.productos = productos;
    }
}
